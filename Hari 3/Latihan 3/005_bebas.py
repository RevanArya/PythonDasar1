import turtle

# Membuat objek Turtle
t = turtle.Turtle()
turtle.bgcolor("black")
t.speed(1)  # Kecepatan menggambar

#OUTLINE
t.pencolor("white")
t.penup()
t.goto(120, 255)
t.pendown()
t.fillcolor("white")
t.begin_fill()
t.goto(-215, -100)
t.goto(-170, -145)
t.goto(235, 180)
t.end_fill()
t.penup()
t.goto(120, -255)
t.pendown()
t.fillcolor("white")
t.begin_fill()
t.goto(-215, 100)
t.goto(-170, 145)
t.goto(235, -180)
t.end_fill()
t.penup()
t.goto(115, 250)
t.pendown()
t.fillcolor("white")
t.begin_fill()
t.goto(115, -250)
t.goto(235, -180)
t.goto(235, 180)
t.goto(125, 250)
t.end_fill()

#BASE COLOR
t.pencolor("steelblue")
t.penup()
t.goto(120, 250)
t.pendown()
